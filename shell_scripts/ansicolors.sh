#!/bin/sh

# Include this via '. ansicolors.sh' and use $Cxxx ${Cxxx} $COFF to
# echo out colored text in your shell scripts

# ANSI color codes
CBLK=`echo -e '\e[01;30m'`
CRED=`echo -e '\e[01;31m'`
CGRN=`echo -e '\e[01;32m'`
CYEL=`echo -e '\e[01;33m'`
CBLU=`echo -e '\e[01;34m'`
CPUR=`echo -e '\e[01;35m'`
CCYA=`echo -e '\e[01;36m'`
CWHT=`echo -e '\e[01;37m'`
COFF=`echo -e '\e[00m'`

